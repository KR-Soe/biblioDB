    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtIsbn.getText());
        
        if(cbnTipo.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "SELECCIONE TIPO");
            }  
        
        if (id < 1) {
            JOptionPane.showMessageDialog(rootPane, "id NO VALIDO");
            return;
        }
        String nombre = txtNombre.getText();
        if (nombre.length() <= 0) {
            JOptionPane.showMessageDialog(rootPane, "nombre NO VALIDO");
            return;
        }
        int anno = Integer.parseInt(txtAño.getText());
        if (anno <= 2017) {
                JOptionPane.showMessageDialog(rootPane, "AÑO NO VALIDO");
                return;
        }
        String editorial = txtEditorial.getText();
        if (editorial.length() <= 0){
                JOptionPane.showMessageDialog(rootPane, "EDITORIAL NO VALIDO");
                return;
        }
        String autor = txtAutor.getText();
        if(autor.length() <= 0){
                JOptionPane.showMessageDialog(rootPane, "AUTOR NO VALIDO");
                return;
        }
        Libro a = new Libro(id, anno, nombre, autor, editorial, autor, rootPaneCheckingEnabled, id);

        Controlador con = new Controlador();
        if (con.modificar(a)) {
            JOptionPane.showMessageDialog(rootPane, "Registro MODIFICADO!!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Hubo un problema");
        }

        limpiar();
    }//GEN-LAST:event_btnModificarActionPerformed

