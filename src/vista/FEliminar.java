    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtId.getText());
        Controlador c = new Controlador();
        Libro alumno = c.leer(id); //recupera objeto alumno si lo encuentra
        if (alumno != null) {
            lblMensaje.setText(alumno.toString());
            int jop = JOptionPane.showConfirmDialog(rootPane,"Est√° Seguro de eliminar a "+alumno.getNombre(), "Elimina", 2);            
            if (jop==0) {
                c.eliminar(id);
                JOptionPane.showMessageDialog(rootPane, "ELIMINADO");
            }           
            if (jop==2) {                
                JOptionPane.showMessageDialog(rootPane, "OPERAICON CANCELADA");
            }
        }else{
                JOptionPane.showMessageDialog(rootPane, "registro no existe");
        }
        lblMensaje.setText("");
    }//GEN-LAST:event_btnEliminarActionPerformed

